# 3-й список: [2, 3, 33, 35, 43, 54, 73, 74, 78, 88, 3, 32, 49, 59, 61, 62, 65, 68, 73, 77]
# 3-й список, содержащий элементы обоих списков без повторений: [2, 3, 32, 33, 35, 43, 49, 54, 59, 61, 62, 65, 68, 73, 74, 77, 78, 88]
# 4-й список, содержащий элементы, общие для двух списков: [3, 73]
# 5-й список, содержащий только уникальные элементы каждого из списков: [2, 32, 33, 35, 43, 49, 54, 59, 61, 62, 65, 68, 74, 77, 78, 88]
# 6-й список, содержащий только минимальное и максимальное значение каждого из списков: [2, 3, 77, 88]